package io.syndesis.extension.twilio;

public class TwilioMsg{

	private String textmsg;
	private String phoneNotoSend;
	
	public String getTextmsg() {
		return textmsg;
	}
	public void setTextmsg(String textmsg) {
		this.textmsg = textmsg;
	}
	public String getPhoneNotoSend() {
		return phoneNotoSend;
	}
	public void setPhoneNotoSend(String phoneNotoSend) {
		this.phoneNotoSend = phoneNotoSend;
	}
	
	
	
	
}
